import math

# 取绝对值
print(abs(-5))
# 以浮点数格式返回绝对值
print(math.fabs(-5))

# 返回最大值
print(max(2, 3, 8))
print(max([1, 3, 5, 7]))

# 返回最小值
print(min(1, 9))

# 四舍五入。第二个参数指定小数位数， 没有参数返回整数
print(round(3.1415))
print(round(3.1465, 2))

# 向上取整
print(math.ceil(5.12))

# 向下取整
print(math.floor(5.9999))

print(2 ** 3)
# 指数运算
print(pow(2, 3))

# 自然对数， e的多少次方
print(math.exp(1))

# 返回对数
print(math.log(100, 10))
print(math.log10(100))












